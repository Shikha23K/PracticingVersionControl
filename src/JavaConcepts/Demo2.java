package JavaConcepts;

public class Demo2 {

	public static void main(String[] args) {
		int n=28;
		int m=15;
		int res=n+m;
		System.out.println("The sum is :"+res);
		res=n-m;
		System.out.println("The subtraction is :"+res);
		res=n/m;
		System.out.println("The quotient is :"+res);
		res=n%m;
		System.out.println("The remainder is :"+res);
		
		float p=87.876f;
		float q=15.8f;
		float res1=p+q;
		System.out.println("The sum is :"+res1);
		res1=p-q;
		System.out.println("The subtraction is :"+res1);
		res1=p/q;
		System.out.println("The quotient is :"+res1);
		res1=p%q;
		System.out.println("The remainder is :"+res1);

	}

}
